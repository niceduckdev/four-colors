    public static boolean isValid(Card lastCard, Card nextCard) {
        boolean type = lastCard.getType() == nextCard.getType();
        boolean color = lastCard.getColor() == nextCard.getColor();
        boolean special = isSpecial(nextCard);

        return (type || color || special);
    }

    public static boolean isPlus(Card card) {
        return (
            card.getType() == CardType.PlusTwo || card.getType() == CardType.PlusFour
        );
    }

    public static boolean isSpecial(Card card) {
        return (
            card.getType() == CardType.ChooseColor || card.getType() == CardType.Reverse || card.getType() == CardType.Skip || isPlus(card)
        );
    }
